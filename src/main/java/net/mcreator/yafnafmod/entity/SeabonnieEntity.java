	public SeabonnieEntity(EntityType<SeabonnieEntity> type, Level world) {
		super(type, world);
		xpReward = 0;
		setNoAi(false);
		setMaxUpStep(0.6f);
		this.setPathfindingMalus(BlockPathTypes.WATER, 0);
		this.moveControl = new MoveControl(this) {
			@Override
			public void tick() {
				if (SeabonnieEntity.this.isInWater())
					SeabonnieEntity.this.setDeltaMovement(SeabonnieEntity.this.getDeltaMovement().add(0, 0.005, 0));
				if (this.operation == MoveControl.Operation.MOVE_TO && !SeabonnieEntity.this.getNavigation().isDone()) {
					double dx = this.wantedX - SeabonnieEntity.this.getX();
					double dy = this.wantedY - SeabonnieEntity.this.getY();
					double dz = this.wantedZ - SeabonnieEntity.this.getZ();
					float f = (float) (Mth.atan2(dz, dx) * (double) (180 / Math.PI)) - 90;
					float f1 = (float) (this.speedModifier * SeabonnieEntity.this.getAttribute(Attributes.MOVEMENT_SPEED).getValue());
					SeabonnieEntity.this.setYRot(this.rotlerp(SeabonnieEntity.this.getYRot(), f, 10));
					SeabonnieEntity.this.yBodyRot = SeabonnieEntity.this.getYRot();
					SeabonnieEntity.this.yHeadRot = SeabonnieEntity.this.getYRot();
					if (SeabonnieEntity.this.isInWater()) {
						SeabonnieEntity.this.setSpeed((float) SeabonnieEntity.this.getAttribute(Attributes.MOVEMENT_SPEED).getValue());
						float f2 = -(float) (Mth.atan2(dy, (float) Math.sqrt(dx * dx + dz * dz)) * (180 / Math.PI));
						f2 = Mth.clamp(Mth.wrapDegrees(f2), -85, 85);
						SeabonnieEntity.this.setXRot(this.rotlerp(SeabonnieEntity.this.getXRot(), f2, 5));
						float f3 = Mth.cos(SeabonnieEntity.this.getXRot() * (float) (Math.PI / 180.0));
						SeabonnieEntity.this.setZza(f3 * f1);
						SeabonnieEntity.this.setYya((float) (f1 * dy));
					} else {
						SeabonnieEntity.this.setSpeed(f1 * 0.05F);
					}
				} else {
					SeabonnieEntity.this.setSpeed(0);
					SeabonnieEntity.this.setYya(0);
					SeabonnieEntity.this.setZza(0);
				}
			}
		};
	}

